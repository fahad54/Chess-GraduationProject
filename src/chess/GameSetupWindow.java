    private void isBonusCheckBoxActionPerformed(java.awt.event.ActionEvent evt) {
        boolean isBonus = isBonusCheckBox.isSelected();
        if (isBonus) {
            bonusTimeOne.setEnabled(true);
            bonusTimeTen.setEnabled(true);
            bonusTimeThirty.setEnabled(true);
            bonusTimeSixty.setEnabled(true);
        } else {
            bonusTimeOne.setEnabled(false);
            bonusTimeTen.setEnabled(false);
            bonusTimeThirty.setEnabled(false);
            bonusTimeSixty.setEnabled(false);
        }
    }
