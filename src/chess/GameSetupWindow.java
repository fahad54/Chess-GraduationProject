    private void playButtonActionPerformed(java.awt.event.ActionEvent evt) {
        Player playerOne;
        Player playerTwo;
        gameWindow = new GameWindow();
        gameWindow.setLocationRelativeTo(this);
        gameWindow.setVisible(true);

        if (playerOneComputerCheckbox.isSelected()) {
            playerOne = new ComputerPlayer();
        } else {
            playerOne = new HumanPlayer(gameWindow);
        }
        if (playerTwoComputerCheckbox.isSelected()) {
            playerTwo = new ComputerPlayer();
        } else {
            playerTwo = new HumanPlayer(gameWindow);
        }
        int whiteTime = Integer.parseInt(whiteTimeField.getText());
        int blackTime = Integer.parseInt(blackTimeField.getText());
        int bonus = 0;
        if (whiteTime < 0) {
            System.out.println("Input Error");
        }
        if (blackTime < 0) {
            System.out.println("Input Error");
        }
        if (isBonusCheckBox.isSelected()) {
            if (bonusTimeOne.isSelected()) {
                bonus = 1;
            }
            if (bonusTimeTen.isSelected()) {
                bonus = 10;
            }
            if (bonusTimeThirty.isSelected()) {
                bonus = 30;
            }
            if (bonusTimeSixty.isSelected()) {
                bonus = 60;
            }
        } else {
            bonus = 0;
        }
        TimeConditions tc;
        if (secCheck.isSelected()) {
            tc = new TimeConditions(whiteTime, blackTime, bonus, false);
        } else {
            tc = new TimeConditions(whiteTime, blackTime, bonus, true);
        }
        gameWindow.startGame(playerOne, playerTwo, tc);
        dispose();
    }

